package operators;

public class ArithmaticDemo {
    public static void main(String[] args) {
            int i1 = -10;
            int i2 = 10;
            int i3 = 10;
            int i4 = 10;
            int i5 = 10;
            double db1 = 14.5d;
            int i6 = 10;
            // operand = i and j
            // operator = +
            System.out.println(i1); // 17
            System.out.println(i2++); // 3
            System.out.println(++i3); // 70
            System.out.println(i4--); // 1
            System.out.println(--i5); // 3
        System.out.println(db1++);
        System.out.println(i6++);
    }
}

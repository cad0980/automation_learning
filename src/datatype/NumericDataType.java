package datatype;

public class NumericDataType {
    public static void main(String[] args) {
        int i = 0x10;
        System.out.println(i);

        // 0 before any number = octal number
        // 0b - binary number
        // 0X - hexadecimal

        // byte, short, int , long
        // default - int
        int i1 = 4538;
        int j = 12432;
        long k1 = 4535345353534534L;

    }
}
